class displayDiv{

    constructor (div){

        this.div = div
    }

    block(){

        this.div.style.display = 'block'

    }

    none(){

        this.div.style.display = 'none'

    }

    flex(){

        this.div.style.display = 'flex'

    }

    tableRow(){

        this.div.style.display = 'table-row'

    }

}
